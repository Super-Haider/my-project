/**
 * @brief Timer Period Elapsed Callback (for interrupt-based method)
 * This function is called every time the timer overflows
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    // Check if this is TIM3
    if (htim->Instance == TIM3)
    {
        // Update PWM value
        led_pwm_value += (fade_direction * FADE_STEP);
        
        // Check boundaries and reverse direction
        if (led_pwm_value >= PWM_MAX_VALUE)
        {
            led_pwm_value = PWM_MAX_VALUE;
            fade_direction = -1;  // Start fading out
        }
        else if (led_pwm_value <= PWM_MIN_VALUE)
        {
            led_pwm_value = PWM_MIN_VALUE;
            fade_direction = 1;   // Start fading in
        }
        
        // Update the PWM compare register
        LED_SetBrightness(led_pwm_value);
    }
}

